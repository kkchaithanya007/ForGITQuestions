package forGIT_Questions;

public class ChangeToLowerCase 
{
/*
 * 34. Write a Java program to convert all the characters in a string to lowercase.
 */
	public static void main(String[] args) {
		String s="HOLA amigo";
		System.out.println("changing the String="+s+" to a lowercase");
		System.out.println("Changed String  ===>"+s.toLowerCase());
	}
}
